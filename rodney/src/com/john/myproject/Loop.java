package com.john.myproject;

public class Loop {

	public static void main(String[] args) {
		//for loop
		// for each
		// while loop
		int i = 0;
		while(i<10) {
			i++;
			System.out.println(i);
		}

		i = 5;
		do { // do-while run the body at least once
			i++;
			System.out.println(i);
		} while(i<1);
	}

}
